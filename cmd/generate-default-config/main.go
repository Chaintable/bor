var (
	// TOML keys supported here:
	// - quoted keys: "a.b", 'a.b'
	// - dotted bare keys: a.b.c
	// - bare key segments: [A-Za-z0-9_-]+
	keyValueRe = regexp.MustCompile(`^\s*("([^"\\]|\\.)*"|'[^']*'|[A-Za-z0-9_-]+(\.[A-Za-z0-9_-]+)*)\s*=`)
)

type docsIndex struct {
	keyDocs     map[string]string
	sectionDocs map[string]string
}

func main() {
	var (
		root   string
		dryRun bool
	)

	flag.StringVar(&root, "root", ".", "Repository root")
	flag.BoolVar(&dryRun, "dry-run", false, "Only print files that would be updated")
	flag.Parse()

	targets, err := collectTargets(root)
	if err != nil {
		exitErr(err)
	}
	if len(targets) == 0 {
		exitErr(fmt.Errorf("no config.toml files found"))
	}

	docIndex, err := buildDocsIndex(filepath.Join(root, "internal", "cli", "server", "config.go"))
	if err != nil {
		exitErr(err)
	}

	updated := 0
	for _, target := range targets {
		current, err := os.ReadFile(target)
		if err != nil {
			if os.IsNotExist(err) {
				current = nil
			} else {
				exitErr(fmt.Errorf("read %s: %w", target, err))
			}
		}

		defaultConfig := loadDefaultConfig()
		renderedConfigText, err := renderConfigWithComments(defaultConfig, docIndex)
		if err != nil {
			exitErr(err)
		}

		if bytes.Equal(current, renderedConfigText) {
			continue
		}

		if dryRun {
			fmt.Printf("would update %s\n", target)
			updated++
			continue
		}

		if err := os.WriteFile(target, renderedConfigText, 0o600); err != nil {
			exitErr(fmt.Errorf("write %s: %w", target, err))
		}
		fmt.Printf("updated %s\n", target)
		updated++
	}

	if dryRun {
		fmt.Printf("dry-run complete: %d files would be updated\n", updated)
		return
	}

	fmt.Printf("complete: %d files updated\n", updated)
}

func collectTargets(root string) ([]string, error) {
	target := filepath.Join(root, "docs", "cli", "default_config.toml")
	if err := os.MkdirAll(filepath.Dir(target), 0o755); err != nil {
		return nil, fmt.Errorf("ensure target directory: %w", err)
	}
	return []string{target}, nil
}

func loadDefaultConfig() *server.Config {
	cfg := server.DefaultConfig()
	cfg.Chain = ""
	cfg.Identity = ""
	cfg.DataDir = "/var/lib/bor"
	return cfg
}

func renderConfigWithComments(cfg *server.Config, docIndex docsIndex) ([]byte, error) {
	fillRawFields(cfg)

	var buf bytes.Buffer
	if err := toml.NewEncoder(&buf).Encode(cfg); err != nil {
		return nil, fmt.Errorf("encode config: %w", err)
	}

	commented, err := annotateTOML(buf.String(), docIndex)
	if err != nil {
		return nil, err
	}

	header := strings.Join([]string{
		"# Auto-generated by cmd/generate-default-config/main.go",
		"# Source of truth: internal/cli/server/DefaultConfig and struct field docs.",
		"",
		"",
	}, "\n")

	return []byte(header + commented), nil
}

