func buildDocsIndex(configPath string) (docsIndex, error) {
	fset := token.NewFileSet()
	file, err := parser.ParseFile(fset, configPath, nil, parser.ParseComments)
	if err != nil {
		return docsIndex{}, fmt.Errorf("parse config source: %w", err)
	}

	structs := map[string]*ast.StructType{}
	for _, decl := range file.Decls {
		gen, ok := decl.(*ast.GenDecl)
		if !ok || gen.Tok != token.TYPE {
			continue
		}
		for _, spec := range gen.Specs {
			ts, ok := spec.(*ast.TypeSpec)
			if !ok {
				continue
			}
			st, ok := ts.Type.(*ast.StructType)
			if !ok {
				continue
			}
			structs[ts.Name.Name] = st
		}
	}

	keyDocs := map[string]string{}
	sectionDocs := map[string]string{}
	visited := map[string]bool{}

	var walkStruct func(typeName string, prefix []string)
	walkStruct = func(typeName string, prefix []string) {
		st, ok := structs[typeName]
		if !ok {
			return
		}

		visitKey := typeName + ":" + strings.Join(prefix, ".")
		if visited[visitKey] {
			return
		}
		visited[visitKey] = true

		for _, field := range st.Fields.List {
			if len(field.Names) == 0 {
				continue
			}
			tagValue := parseTag(field.Tag)
			tagName, isBlock := parseTOMLTag(tagValue)
			if tagName == "" || tagName == "-" {
				continue
			}

			doc := normalizeDoc(field.Doc, field.Comment)
			path := joinPath(prefix, tagName)

			if isBlock {
				if doc != "" {
					sectionDocs[path] = doc
				}
				if nested := resolveStructName(field.Type); nested != "" {
					walkStruct(nested, append(prefix, tagName))
				}
				continue
			}

			if doc != "" {
				keyDocs[path] = doc
			}
		}
	}

	walkStruct("Config", nil)
	addRawFieldDocs(keyDocs)

	return docsIndex{keyDocs: keyDocs, sectionDocs: sectionDocs}, nil
}

func annotateTOML(content string, index docsIndex) (string, error) {
	scanner := bufio.NewScanner(strings.NewReader(content))
	lines := make([]string, 0, 512)
	section := []string{}

	for scanner.Scan() {
		line := scanner.Text()
		trimmed := strings.TrimSpace(line)
		if trimmed == "" {
			lines = append(lines, line)
			continue
		}

		if strings.HasPrefix(trimmed, "[") && strings.HasSuffix(trimmed, "]") {
			section = parseSection(trimmed)
			sectionPath := strings.Join(section, ".")
			if doc := index.sectionDocs[sectionPath]; doc != "" {
				lines = append(lines, formatComment(doc, leadingWhitespace(line))...)
			}
			lines = append(lines, line)
			continue
		}

		keyPath, ok := parseKeyPath(line, section)
		if !ok {
			lines = append(lines, line)
			continue
		}

		if doc := index.keyDocs[keyPath]; doc != "" {
			lines = append(lines, formatComment(doc, leadingWhitespace(line))...)
		}
		lines = append(lines, line)
	}

	if err := scanner.Err(); err != nil {
		return "", fmt.Errorf("scan toml: %w", err)
	}

	return strings.Join(lines, "\n") + "\n", nil
}

func formatComment(doc string, indent string) []string {
	doc = strings.TrimSpace(doc)
	if doc == "" {
		return nil
	}

	return []string{indent + "# " + doc}
}

func leadingWhitespace(line string) string {
	i := 0
	for i < len(line) && (line[i] == ' ' || line[i] == '\t') {
		i++
	}
	return line[:i]
}

func parseKeyPath(line string, section []string) (string, bool) {
	m := keyValueRe.FindStringSubmatch(line)
	if len(m) < 2 {
		return "", false
	}
	key := strings.TrimSpace(m[1])
	key = trimTOMLQuotes(key)

	if len(section) == 0 {
		return key, true
	}
	parts := append(append([]string{}, section...), key)
	return strings.Join(parts, "."), true
}

func parseSection(line string) []string {
	if strings.HasPrefix(line, `["`) && strings.HasSuffix(line, `"]`) {
		return []string{trimTOMLQuotes(strings.Trim(line, "[]"))}
	}
	if strings.HasPrefix(line, `['`) && strings.HasSuffix(line, `']`) {
		return []string{trimTOMLQuotes(strings.Trim(line, "[]"))}
	}

	inside := strings.Trim(line, "[]")
	rawParts := strings.Split(inside, ".")
	parts := make([]string, 0, len(rawParts))
	for _, raw := range rawParts {
		parts = append(parts, trimTOMLQuotes(strings.TrimSpace(raw)))
	}
	return parts
}

func trimTOMLQuotes(s string) string {
	s = strings.TrimSpace(s)
	if len(s) >= 2 {
		if (s[0] == '"' && s[len(s)-1] == '"') || (s[0] == '\'' && s[len(s)-1] == '\'') {
			return s[1 : len(s)-1]
		}
	}
	return s
}

