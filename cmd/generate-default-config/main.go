func parseTag(tagLit *ast.BasicLit) string {
	if tagLit == nil {
		return ""
	}
	return strings.Trim(tagLit.Value, "`")
}

func parseTOMLTag(tag string) (name string, isBlock bool) {
	if tag == "" {
		return "", false
	}

	chunks := strings.Split(tag, " ")
	for _, chunk := range chunks {
		chunk = strings.TrimSpace(chunk)
		if !strings.HasPrefix(chunk, `toml:"`) {
			continue
		}
		value := strings.TrimPrefix(chunk, `toml:"`)
		value = strings.TrimSuffix(value, `"`)
		parts := strings.Split(value, ",")
		if len(parts) == 0 {
			return "", false
		}
		name = strings.TrimSpace(parts[0])
		for _, p := range parts[1:] {
			if strings.TrimSpace(p) == "block" {
				isBlock = true
			}
		}
		return name, isBlock
	}
	return "", false
}

func resolveStructName(expr ast.Expr) string {
	switch t := expr.(type) {
	case *ast.StarExpr:
		if id, ok := t.X.(*ast.Ident); ok {
			return id.Name
		}
	case *ast.Ident:
		return t.Name
	}
	return ""
}

func normalizeDoc(groups ...*ast.CommentGroup) string {
	for _, group := range groups {
		if group == nil {
			continue
		}
		lines := make([]string, 0, len(group.List))
		for _, c := range group.List {
			txt := strings.TrimSpace(strings.TrimPrefix(c.Text, "//"))
			txt = strings.TrimSpace(strings.TrimPrefix(txt, "/*"))
			txt = strings.TrimSpace(strings.TrimSuffix(txt, "*/"))
			if txt == "" {
				continue
			}
			lines = append(lines, txt)
		}
		if len(lines) == 0 {
			continue
		}
		return strings.Join(lines, " ")
	}
	return ""
}

func joinPath(prefix []string, key string) string {
	if len(prefix) == 0 {
		return key
	}
	parts := append(append([]string{}, prefix...), key)
	return strings.Join(parts, ".")
}

func exitErr(err error) {
	fmt.Fprintf(os.Stderr, "error: %v\n", err)
	os.Exit(1)
}
